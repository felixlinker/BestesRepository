def vertices_are_compatible(label_vertex1, label_vertex2):
    if label_vertex1 in ["a", "b"] and label_vertex2 in ["a", "b"]:
        return True
    else:
        return False

def edges_are_compatible(label_edge1, label_edge2):
    if label_edge1 in ["a", "b"] and label_edge2 in ["a", "b"]:
        return True
    else:
        return False
